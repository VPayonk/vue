<template>
    <h1>Counter: {{ counter }}</h1>
</template>

<script>
  import {eventEmitter} from './main';

  export default {
    // props: [
    //     'counter'
    // ]
    data() {
      return {
        counter: 0
      }
    },
    created() {
      eventEmitter.$on('updateCounter', (number) => {
        this.counter += number;
      })
    }
  };
</script>
